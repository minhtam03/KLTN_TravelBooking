import mongoose from "mongoose";

const tourSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },
    city: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    distance: {
      type: Number,
      required: true,
    },
    photo: {
      type: String,
    },
    desc: {
      type: String,
      required: true,
    },
    duration: {
      type: Number,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    maxGroupSize: {
      type: Number,
      required: true,
    },

    reviews: [
      {
        type: mongoose.Types.ObjectId,
        ref: "Review",
      },
    ],

    featured: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

tourSchema.statics.getAvailableTours = async function (destination, duration) {
  try {
    return await this.find({ city: destination, duration: duration });
  } catch (error) {
    console.error("Error fetching available tours: ", error);
    throw new Error('Error fetching available tours');
  }
};

const Tour = mongoose.model('Tour', tourSchema);

export default mongoose.model("Tour", tourSchema);
